package javaSE.Primitive;

public class PrimitiveType {
    public static void main(String[] args) {
        System.out.println("Byte.MAX_VALUE:"+Byte.MAX_VALUE);
        System.out.println("Byte_min_value:"+Byte.MIN_VALUE);
        System.out.println("-------------------------------");
        System.out.println("Short_MAX_VALUE"+Short.MAX_VALUE);
        System.out.println("Short_MIN_VALUE"+Short.MIN_VALUE);
        System.out.println("--------------------------------");
        System.out.println("Int_MAX_VALUE"+Integer.MAX_VALUE);
        System.out.println("Int_min_value"+ Integer.MIN_VALUE);
        System.out.println("---------------------------------");
        System.out.println("Long_MAX_value"+ Long.MAX_VALUE);
        System.out.println("Long_min_value"+ Long.MIN_VALUE);
        System.out.println("-------------------------------");
        System.out.println("Float_MAX_value"+Float.MAX_VALUE);
        System.out.println("Float_MIN_value"+Float.MIN_VALUE);
        System.out.println("--------------------------------");
        System.out.println("Double_max_value"+Double.MAX_VALUE);
        System.out.println("Double_min_value"+Double.MIN_VALUE);
        System.out.println("---------------------------------");
    }
}
